public class Carro {
    // Declaração de variáveis privadas
    private String modelo; // Variável para armazenar o modelo do carro
    private int ano; // Variável para armazenar o ano do carro

    // Método getter para a variável 'modelo'
    public String getModelo() {
        return modelo; // Retorna o valor atual do modelo
    }

    // Método setter para a variável 'modelo'
    public void setModelo(String modelo) {
        this.modelo = modelo; // Define o valor do modelo
    }

    // Método getter para a variável 'ano'
    public int getAno() {
        return ano; // Retorna o valor atual do ano
    }

    // Método setter para a variável 'ano'
    public void setAno(int ano) {
        this.ano = ano; // Define o valor do ano
    }
}